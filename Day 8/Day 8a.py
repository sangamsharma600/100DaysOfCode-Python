#Function with only one parameter#

def greet(name):
    print(f"Hello {name}")
    print(f"{name}, this is an example of function")

greet('Sangam')

#Function with more than one parameter#

def home_address(name,address):
    print(f"Hello, {name} lives in {address}")

home_address(name='Sangam',address= 'Dharna')